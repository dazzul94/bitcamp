package bitcamp.java100;
public class Test12_4 {
    public static void main(String[] args) {    
        // 변수 선언
        int i ;
        i = 100;
        i = 200;//변수는 언제든지 값을 바꿀 수 있다

        //상수 선언 'final'이라는 modifier를 변수 선언 앞에 붙인다
        final int j ;
        j = 200; //처음 한 번 값 저장할 수 있다
        // j = 300; 기존 값을 변경할 수는 없다

    }
}